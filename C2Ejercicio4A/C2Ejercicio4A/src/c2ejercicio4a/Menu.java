    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int opc = this.combo1.getSelectedIndex();
        double cantidad = Double.parseDouble(this.txtCantidad.getText());
        double precio = Double.parseDouble(this.txtPrecio.getText());
        switch(opc){
                case 1 -> {
                    b=new AguaMineral("Agua Mineral", cantidad, precio, "PeÃ±afiel");
                    metodos.agregarBebida(b);
                }
                case 2 -> {
                    b=new BebidaAzucarada(0.2, true, cantidad, precio, "Coca Cola");
                    metodos.agregarBebida(b);
                }
            }
        
    }//GEN-LAST:event_btnAgregarActionPerformed
   
